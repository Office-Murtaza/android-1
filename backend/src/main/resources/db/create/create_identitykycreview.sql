CREATE TABLE IF NOT EXISTS w_identitykycreview (
id BIGINT(20) NOT NULL AUTO_INCREMENT,
identity_id BIGINT(20) NOT NULL,
tier_id INT(2) NOT NULL,
review_status INT(2) NOT NULL,
file_name VARCHAR(255) DEFAULT NULL,
file_mimetype VARCHAR(50) DEFAULT NULL,
id_card_number VARCHAR(255) DEFAULT NULL,
ssn VARCHAR(11) DEFAULT NULL,
first_name VARCHAR(255) DEFAULT NULL,
last_name VARCHAR(255) DEFAULT NULL,
address VARCHAR(255) DEFAULT NULL,
country VARCHAR(255) DEFAULT NULL,
province VARCHAR(255) DEFAULT NULL,
city VARCHAR(255) DEFAULT NULL,
zip_code VARCHAR(255) DEFAULT NULL,
rejected_message VARCHAR(100) DEFAULT NULL,
create_date DATETIME DEFAULT CURRENT_TIMESTAMP,
update_date DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
PRIMARY KEY (id),
FOREIGN KEY (identity_id) REFERENCES identity(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;